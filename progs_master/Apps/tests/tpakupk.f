C$FORTRAN           TSTPAK
      subroutine    TSTPAK (holl, holo)
C+---------------------------------------------------------------------+
C|    Routine to test utility:   PAKCHA ( PAKCHAO )                    |
C+---------------------------------------------------------------------+
C+---------------------------------------------------------------------+
C|                  A R G U M E N T S                                  |
C+---------------------------------------------------------------------+
      integer       holl(*),  holo(*)
C+---------------------------------------------------------------------+
C|                  T Y P E   &   D I M E N S I O N                    |
C+---------------------------------------------------------------------+
      integer       i,        j,        k,        off

      character     string*12
C+---------------------------------------------------------------------+
C|                  D A T A                                            |
C+---------------------------------------------------------------------+
      data          string /'abCD0123$(/*'/
C+---------------------------------------------------------------------+
C|                  E X T E R N A L S                                  |
C+---------------------------------------------------------------------+
*     external      CC2H
*     external      CC2HO
C+---------------------------------------------------------------------+
C|                  L O G I C                                          |
C+---------------------------------------------------------------------+

C     test the function:  CC2H (cha, hol, num)
C     ----------------------------------------

      write (6,2000) string

      do 10 i = 1,5
        holl(i) =  0
   10 continue

      write (6,2001) (holl(j),j=1,3)

      call     CC2H (string, holl, 12)

      write (6,2002) (holl(j),j=1,3)

C     test the function:  CC2HO (hol, off, cha, num)
C     ----------------------------------------------

      off  =  2

      write (6,2003) off, string

      do 20 i = 1,5
        holo(i) =  0
   20 continue

      write (6,2004) (holo(j),j=1,4)

      call     CC2HO (string, holo, off, 12)

      write (6,2005) (holo(j),j=1,4)

      return
C+---------------------------------------------------------------------+
C|    Format Statements                                                |
C+---------------------------------------------------------------------+
 2000 format (//5X,'Testing function:  CC2H (c, h, n)',
     $        //5X,'        string "c" = ',A,
     $         /5X,'                     ------------')
 2001 format (  5X,' BEFORE:   holl(-) = ',3(z8,1X))
 2002 format (  5X,'  AFTER:   holl(-) = ',3(z8,1X),
     $         /5X,'                     ',3('-------- '))

 2003 format (//5X,'Testing function:  CC2HO (c, h, off, n)',
     $             '  with off = ',i2,
     $        //5X,'        string "c" = ',A,
     $         /5X,'                     ------------')
 2004 format (/,5X,' BEFORE:   holo(-) = ',4(z8,1X))
 2005 format (  5X,'  AFTER:   holo(-) = ',4(z8,1X),
     $         /5X,'                     ',4('-------- '))

      end
